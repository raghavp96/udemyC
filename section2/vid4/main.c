#include <stdio.h>

// Arithmetical Operators: +, -, *, /, %  (Binary operators: need two operands)

// +, -, * have unary usage as well

int main() {
	int a = 10, b = 2;
	printf("%d + %d = %d\n", a, b, a+b);
	printf("%d - %d = %d\n", a, b, a-b);
	printf("%d * %d = %d\n", a, b, a*b);
	printf("%d / %d = %d\n", a, b, a/b);
	printf("%d %% %d = %d\n", a, b, a%b);

	return 0;	
}


